export function useTranslation(lang = 'pt') {
  const t = (key) => {
    return translations[lang]?.[key] || translations.en[key] || key
  }
  
  const translateCategory = (category) => {
    return categoryTranslations[lang]?.[category] || category
  }
  
  return { t, translateCategory }
}
